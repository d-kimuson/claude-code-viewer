import { z } from "zod";

export const DocumentContentSchema = z.object({
  type: z.literal("document"),
  source: z.union([
    z.object({
      media_type: z.enum([
        "text/plain",
        "text/markdown",
        "text/x-markdown",
        "application/javascript",
        "text/javascript",
        "application/typescript",
        "text/typescript",
        "application/json",
        "text/html",
        "text/css",
        "application/xml",
        "text/xml",
        "text/x-python",
        "text/x-java",
        "text/x-c",
        "text/x-c++",
        "text/x-csharp",
        "text/x-go",
        "text/x-rust",
        "text/x-ruby",
        "text/x-php",
        "text/x-swift",
        "text/x-kotlin",
        "application/x-sh",
        "text/x-shellscript",
        "application/x-yaml",
        "text/yaml",
        "text/x-sql",
      ]),
      type: z.literal("text"),
      data: z.string(),
    }),
    z.object({
      media_type: z.enum(["application/pdf"]),
      type: z.literal("base64"),
      data: z.string(),
    }),
  ]),
});
